        <p>Ibarat roda pada mobil yang mentransfer energi dari mesin dan melesatkan mobil. String pada busur juga memindahkan hentakan energinya kepada arrow hingga terpental melesat menuju target yang di bidik.String busur ini merupakan bagian paling vital dalam peralatan panahan. Untuk itu perlu perhatian dan perawatan hingga penggantian untuk memastikan penggunaan yang aman dan maksimal.</p>
        <p>Bahan konvensional yang digunakan untuk string berupa linen, rami, serat nabati lainnya, otot, dan kulit binatang. Dalam kondisi darurat, hampir semua serat dapat digunakan. Hanya saja serat dari otot dan kulit binatang akan sangat rentan dengan air.</p>
        <P>Akan tetapi hari ini kita dapatkan string bahan sintetis berteknologi tinggi. Serat canggih ini memungkinkan busur menembak lebih handal, lebih cepat, dan akurat.</P>
        <h5>STRING MODERN</h5>
      <p>Di antara string dengan bahan serat modern yang sering digunakan oleh archer pemula adalah DACRON, dikarenakan daya tahan dan peregangannya. Dengan bahan polister biasa, Dacron memiliki kekuatan 50 lb atau setara 22,5 kg per helai (ply). String Dacron punya nilai peregangan yang relatif tinggi (2,6%) ini menyebabkan lebih sedikit goncangan pada busur. Senar Dacron mudah dipelihara dan dapat bertahan relatif lebih lama.</p>
      <p>String modern lainnya ada yang menggunakan bahan polimer kristal cair seperti Kevlar dan Vectran. Bahan polimer ini memiliki kepadatan lebih tinggi dan diameter lebih kecil dari Dacron. Dengan kekuatan 31,8 kg (70 lb) per helai dan nilai peregangan 0,8%, string ini mampu menghasilkan kecepatan panah sekitar 2 meter/detik.</p>
      <p>Polietilena dengan berat molekul sangat tinggi (UHMWPE – Ultra High Molecular Weight Polyethylene, seperti Spectra dan Dyneema, memiliki kekuatan 45,5 kg (100 lb) per helainya. Selain itu memiliki tingkat peregangan 1 %. String berbahan ini lebih ringan dan lebih cepat dari string berbahan Kevlar. Selain juga lebih tahan lama.</p>
      <p>Terkadang string modern dibuat dari campuran fiber komposit campuran dari Vectran dan Dyneema, untuk mendapatkan keunggulan dari masing-masing bahan.
        Belakangan penggolongan jenis string ini menyempit menjadi 2 saja : Dacron dan Fast Flight. Fast Flight adalah merk string produksi Brownell. Mungkin ini semacam penyebutan “Sanyo” sebagai ganti penyebutan jenis produk “pompa air” bahkan meski menggunakan merk lainnya.
        Dacron diistilahkan untuk string yang punya nilai regang / melar lebih besar dari Fast Flight.
        Sedangkan Fast Flight diistilahkan untuk semua jenis bahan string modern yang punya nilai regang / melar lebih kecil dari Dacron.
        </p>
