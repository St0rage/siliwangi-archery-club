            <h5>STRING RECURVEBOW dan LONGBOW</h5>
            <P>Ketika membeli sebuah string, kita musti tahu panjang busur. Hal ini bisa dengan mengukurnya dari cekungan di ujung busur (limb) ke cekungan di ujung lainnya. Pada busur recurve modern(standarbow dan FITA), panjang busur biasanya tertera pada badan limb dengan ukuran inch.Sekali Anda mengetahui ukuran panjang busur, maka Anda bisa menentukan panjang string untuk busur Anda.Pada busur recurve, biasanya panjang string lebih pendek 4 inch dari panjang busur. Sedangkan pada longbow, panjangnya 3 inch lebih pendek dari panjang busur.</P>
            <P>Jika belum juga yakin dengan pengukuran Anda, datang saja ke toko archery terdekat untuk mempelajari panjang string. Di BOSS Archery Anda bisa bebas bertanya dan meminta saran terbaik untuk kebutuhan archery equipment Anda.Kemudian, tentukan berapa banyak helai (ply) bahan string yang Anda butuhkan. Ini tergantung pada jenis bahan string dan draw weight pada busur Anda.</P>
            <p>Draw weight yang besar akan membutuhkan lebih banyak helai untuk menanggung beban tersebut. Dan draw weight yang lebih kecil akan memerlukan helai lebih sedikit pula. Konsultasikan hal ini pada pelatih atau toko archery langganan Anda.Untuk recurvebow dan longbow terdapat dua tipe berdasarkan cara pembuatannya : FLEMISH TWIST dan ENDLESS LOOP. Flemish twist pada lubang di kedua ujungnya berupa jalinan/kepangan. Memberikan kesan tradisional yang menarik. Sedangkan pada Endless loop, lubang di kedua ujungnya dilapisi dengan lilitan benang serving.String Flemish twist sering digunakan pada tradbow. Menambah kesan estetika dan keindahan pada busur tradisional. Sedangkan string Endless loop sering kita jumpai digunakan pada busur pemanah target. Dan tipe string terakhir ini, selain menyediakan string siap pakai, BOSS Archery sudah bisa membuat custom sesuai kebutuhan customer.</p>
            <h5>STRING COMPOUNDBOW</h5>
            <p>String pada busur compound terdiri dari beberapa bagian. String busur itu sendiri dan 1 kabel atau lebih yang menghubungkan antar cam. Panjang string dan kabel biasanya tertulis di busur atau di buku manual yang disertakan. Jika Anda tidak menemukannya, bawa busur Anda ke toko panahan untuk mendapatkan informasi yang tepat.</p>
            <h5>PERAWATAN STRING</h5>
            <p>Tentu saja usia string kita sangat bergantung dengan intensitas penggunaan. Namun dengan mengikuti beberapa tips berikut akan bisa membantu memperpanjang usia pemakaian string :</p>
            <ul>
                <li>Beri Bowstring-WAX (pelumas) pada string secara teratur.</li>
                <li>Sebelum berlatih tiap hari, periksalah string, gerakkan jemari Anda pada string ke atas dan ke bawah untuk memeriksa jisa saja string terasa kering. Olesi dengan WAX dan tekanlah dengan jemari Anda hingga benar-benar merasuk ke dalam string.</li>
                <li>Jangan meletakkan busur di bawah terik panas. Hindarkan sebisa mungkin dari panas ketika usai sesi memanah.</li>
                <li>Pada recurvebow dan longbow, untuk menghindari peregangan string sepanjang waktu, sebaiknya string dilepas ketika busur tidak lagi digunakan berlatih.</li>
            </ul>
