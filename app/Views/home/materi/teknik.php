        <h2>Teknik Dasar Panahan</h2>
        <p>Secara singkat teknik memanah dalam buku karangan Kesik Lee (2007), disana meyebutkan ada 12 tahapan dalam teknik memanah yaitu:</p>
      <ol>
          <li>Stance</li>
          <li>Nocking The Arrow</li>
          <li>Hooking and Gripping The Bow</li>
          <li>Mindset</li>
          <li>Set-up</li>
          <li>Drawing</li>
          <li>Anchoring</li>
          <li>Aiming and Expension</li>
          <li>Release</li>
          <li>Follow Through</li>
          <li>Feedback</li>
      </ol>

      <h5>1. Teknik Dasar Memanah: Stance</h5>
      <img src="images/teknik/stance.jpg" style="margin-left: 10px;" alt="">
      <p>Stance adalah sikap atau posisi kaki saat berdiri di atas lantai atau tanah. Posisi kaki yang baik pada saat berdiri memiliki 2 syarat, yaitu:</p>
      <ul>
          <li>Syarat Pertama: Titik berat badan saat berdiri ditopang oleh kedua kaki secara seimbang yakni dengan persentase 60 – 70 % bertumpu pada bola kaki sedangkan sisanya 30 – 40 % bertumpu pada tumit.</li>
          <li>Syarat Kedua: Posisi tubuh harus seimbang dan tegak. Dalam artian tidak condong ke depan atau ke belakang dan samping kanan dan samping kiri.</li>
      </ul>

      <p>Agar mencapai tubuh yang seimbang dan sikap berdiri yang benar maka ada 3 hal yang harus diperhatikan yaitu:</p>
      <ul>
          <li>Pertama: Jarak antar kedua kaki harus selebar bahu</li>
          <li>Kedua: Ujung kaki harus menyentuh garis khayal di bagian tengah sasaran target. </li>
          <li>Ketiga: Pastikan kedua lutut dalam kondisi rileks (tidak tegang)</li>
      </ul>
      <p>Sikap posisi kaki yang benar dalam olahraga panahan ada 4 macam, diantaranya:</p>
     <ol>
         <li>Square or Parallel Stance ialah sikap posisi kaki sejajar dengan lantai. Yakni dengan meluruskan kedua ujuang kaki sejajar dengan sasaran target. Sedangkan posisi dada dengan sasaran membentuk sudur 90o.</li>
         <li>Open Stance ialah posisi kaki terhadap lantai terbuka sedikit lebar. Dalam artian posisi kaki kanan sedikit maju ke depan dan kaki kiri sejajar dengan sasaran target sedikit serong ke kiri. Badan membentuk sudut 60o.</li>
         <li>Close Stance ialah sikap atau posisi kaki pada saat berdiri di atas lantai secara tertutup. Maksudnya adalah bagain ujung kaki kanan menyentuh garis khayal dan bagian tumit kaki kiri bersentuhan garis khayal atau sejajar dengan kaki kanan. Dada membentuk sudut 120o.</li>
         <li>Oblique Stance ialah kebalikan dari closes stance yakni bagian ujung kaki depan sejajar dengan bagian ujung kaki kiri dengan arah menyerong menuju tengah sasaran. Dan dada membentuk sudut 45o.</li>
     </ol>

     <h5>2.	Teknik Dasar Memanah: Nocking</h5>
     <img src="images/teknik/NOCKING.png" style="margin-left: 10px;" alt="">
     <p>Nocking adalah gerakan dasar memanah dengan menempatkan atau memasukkan ekor atau ujung panah ke tempat anak panah (nocking point) pada tali busur serta meletakkan gandar (shaft) pada sandaran yang telah disediakan. Agar teknik nocking dapat bekerja dengan baik dan tepat sasaran maka ada 3 aspek yang perlu diperhatikan seorang pemanah yakni:</p>

     <ol>
         <li>Aspek I: Bulu indeks pada bagian ekor panah harus menjauhi sisi jendela busur</li>
         <li>Aspek II: Ekor panah harus tapat masuk ke tali.</li>
         <li>Aspek III: Nocking point (anak panah) harus benar-benar masuk dan pas ke nock. Apabila anak panah terlalu besar maupun longgar maka akan menggangu terbangnya anak panah.</li>
     </ol>

     <h5>3. Teknik Dasar Memanah: Hooking And Gripping The Bow</h5>
     <p>Hooking and Gripping the Bow ialah gerakan memanah dengan menempatkan maupun mengaitkan jari di tali setelah anak panah atau nocking point terpasang dengan benar.Posisi jari harus ditempatkan pada tali dan tali harus diposisikan di sendi pertama, lebih tepatnya pada bagian sendi atas jari telunjuk, di bawah jari tengah dan di belakang sendiri jari manis.</p>
     <p>Perlu diingat jangan mencoba meletakkan tali pada sendi pertama di bagian jari atas dan bawah. Hal ini bisa membahayakan perkembangan sendi.Sebelum menerbangkan atau menembah anak panah maka seorang pemanah harus mengecek terlebih dahulu tab tali antara nocking point dengan posisi jari di tab.</p>
     <p>Agar proses penembakan panah berjalan lancar maka lebih baik menggunakan pembatas jari sehingga jari akan berada pada posisinya dengan benar dan terasa lebih relaks.Apabila tidak menggunakan pembatas jari maka jari akan terasa cenderung menyebar. Sehingga bisa menggangu proses penembakan.</p>

     <h5>4. Teknik Dasar Memanah: Mindset</h5>
     <img src="images/teknik/Mindset.png" style="margin-left: 10px;" alt="">
     <p>    Mindset adalah aspek yang harus dipenuhi oleh seorang pemanah baik dalam kondisi fisik dam taktik.
        Mindset adalah aspek yang paling penting dari semua spek yang ada. Karena seorang pemanah harus benar-benar rilkes disaat menembak anak panah dari busurnya.
        Oleh karenanya seorang pemanah harus lebih aktif dan tetap fokus terhadap berbagai tugas lapangan maupun pertandingan.</p>
        <p>Seorang pemanah yang kontinu melatih dirinya, maka akan senantiasa fokus terhadap tugas yang  sedang dihadapi. Sehingga ketika anak busur diterbangkan maka akan mendapatkan bonus atau skor yang lebih baik.</p>
        
        <h5>5. Teknik Dasar Memanah: Set-Up</h5>
        <img src="images/teknik/Set-up.png" style="margin-left: 10px;" alt="">
        <p>Set-up adalah istilah yang hampir sama dengan pre-draw yakni gerak tarikan awal. Artinya adalah kekuatan tekanan jari tangan pada tali saat tarikan penuh (full-draw) kira-kira 30%, kemudian pada jari tengah sekitar 50% – 60% dan jari manis 20%. Akan tetapi perbedaan antara pre-draw dengan full-draw ialah pada tarikan tekanan di jari-jaringan.</p>
        <p>Posisii yang tepat teknik set up adalah posisi tungkai lurus, rileks, berat badan ditumpu dengan kedua kaki dengan persentase bola kaki dan tumit masing-masing adalah 60%  – 70% dan 30%  – 40%.Perputaran tubuh pada bagian atas (upper body) harus dimulai dari panggul, kemudian bahu diluruskan tepat mengenai target dan panggul diputar agar lurus dengan target.</p>
        <p>Jika pemanah mengggunakan open stance panggul dengan sendirinya terbuka ke arah target. Dan yang perlu dihindari ketika panggul segaris dengan bahu maka akan menyebabkan ketengan otot.Untuk menghasilkan stabilitas yang baik maka tulang dada (stenum) ditekan dan otot perlu ditahan.</p>
        <p>Beberapa keselahan yang sering terjadi oleh pemanah yaitu: badan terlalu condong ke arah target dan pada bagian leher bahu terlalu tegang. Apabila ini dilakukan terus menerus maka akan mengakibatkan kepala selalu menengadah kebelakang.</p>

        <h5>6. Teknik Dasar Memanah: Drawing</h5>
        <img src="images/teknik/drawing.jpg" style="margin-left: 10px;" alt="">
        <p>Drawing adalah teknik memanah dengan melakukan gerakan menarik tali busur sampai menyentuh bagian dagu, bibir dan hidung serta dilanjutkan dengan menjangkarkan tangan penarik tali di dagu. Ada 9 aspek yang perlu diperhatikan apabila menggunakan teknik drawing, diantaranya:</p>
        <ul>
            <li>Saat drawing harus bernapas. Dan saat busur diangkat, ambil napas dengan menggunakan teknik pernapasan diafragma.</li>
            <li>Tarik lurus sesuai dengan lintasan tali hingga menyentuh dagu dari sikap set-up. Sekitar 2-3 inch di bahwah anchor point. Pastikan saat menarik serileks mungkin hingga gerakan menembak.</li>
            <li>Untuk mendapatkan perasaan rileks maka saat menarik tali busur ambil napas secara perlahan-lahan.</li>
            <li>Angel atau sudut tangan kanan bagian belakang harus stabil. Pemanah jangan memutar tangan saat melakuakn drawing. Karena memutar tangan bisa meyebabkan puntiran tali (torque) menjadi lebar.</li>
            <li>Ada dua hal yang perlu diperhatikan yakni lengan penarik dan jari penarik. Kedua hal ini harus dilakukan optimal saat melakukan penarikan. Jida tidak akan menyebabkan jari-jari tangan lemas.</li>
            <li>Saat melakukan drawing, visir harus tepat berada di atas pusat garis horizontal. Ini bertujuan untuk menaikkan lengan panahan busur sebagai tanda bidikan.</li>
            <li>Saat melakukan penarikan busur, bahu tetap berada pada posisinya. Dalam artian tidak terlalu rendah dan juga menonjol.</li>
            <li>Saat lengan panahan busur diluruskan akan tampak berbentuk huruf “V” di ujung bahu. Hal ini dapat terbentuk jika otot tricep pada lengan panahan busur harus kuat agar membantu bow shoulder tidak menonjol.</li>
            <li>Gunakan otot bahu saat menarik busur. Yaitu otot deltoideus posterior, tere major, rhomboideus dan travezius.</li>
        </ul>

        <h5>7. Teknik Dasar Memanah: Anchoring</h5>
        <img src="images/teknik/Anchoring.png" style="margin-left: 10px;" alt="">
        <p>Anchoring adalah gerakan dasar teknik memanah dengan menjangkarkan tangan penarik pada bagian dagu atau rahang.</p>

        <h5>8. Teknik Dasar Memanah: Aiming And Expansion</h5>
        <img src="images/teknik/Aiming-and-Expension.png" style="margin-left: 10px;" alt="">
        <p>Aiming and Expension adalah suatu gerakan dengan mengarahkan atau menempatkan titik alat pembidik (visir) tepat berda pada titik sasaran.Ada 3 aspek yang perlu diperhatikan saat membidik yaitu sikap memanah harus tetap dipertahankan, string alignmet harus tetap dan jangan membidik target terlalu lama</p>
        <p>Aiming baru bisa dimulai jika fase transfer dan holding tercapai. Dan waktu idealnya adalah 1 – 3 detik untuk mendapatkan hasil yang terbaik.</p>

        <h5>9. Teknik Dasar Memanah: Release</h5>
        <img src="images/teknik/Release.png" style="margin-left: 10px;" alt="">
        <p>Release adalah gerakan teknik memanah yakni dengan merilekskan jari-jari penarik tali. Agar anah panah dapat terbang dengan mulus maka pastikan release bekerja dengan baik.Ada dua model release yang dapat digunakan dalam teknik memanah, diantaranya:</p>
        
        <ul>
            <li>Dead release: Suatu teknik memanah disaat tali lepas meninggalkan anchoring. Akan tetapi tangan penarik tetap menempel pada dagu. Seperti halnya sebelum tali di lepas.</li>
            <li>Action atau active release: adalah teknik dimana saat busur dilepas, posisi tangan melewati atau menelusuri dagu dan leher pemanah.</li>
        </ul>
        <p>Dari dua model teknik release diatas, banyak para pemanah atau atlit panah menerepkan teknik active release karena mudah dan iramanya dapat diterapkan dengan baik.</p>

        <h5>10. Teknik Dasar Memanah: Follow – Through</h5>
        <img src="images/teknik/Follow-–-Through.png" style="margin-left: 10px;" alt="">
        <p>Follow – Through adalah bagian dari teknik release. Artinya teknik ini bisa digunakan secara alami atau tidak dibuat secara berlebihan.Follow – Through yang berlebihan akan menyebabkan titik berat panah dan tembakan menjadi kacau. Oleh karenanya seorang pemanah harus bisa mengontrol tembakannya dan perasaan saat menembak busur.</p>

        <h5>11. Teknik Dasar Memanah: Relaksasi Dan Feedback</h5>
        <img src="images/teknik/Relaksasi-dan-Feedback.png" style="margin-left: 10px;" alt="">
        <p>Feed-Back adalah posisi dimana pemanah tidak emosional dalam menganalisis suatu masalah yang muncul. Dalam hal ini pemanah harus bertanggung jawab terhadap pada hasil tembakan.Dan seorang pemanah juga harus bisa merasakan tembakan yang diarahkan ke target. Dengan berpedoman kepada teknik-teknik yang diajarkan.</p>
        
        <h2>Kesimpulan Teknik Memanah</h2>
        <p>Bagian terpenting yang harus dikuasi oleh seorang pemanah adalah sikap memanah atau shooting form yang tidak menyalahi hukum mekanika gerak.Teknik memanah pada umumnya yang diterapin di dunia adalah 9 tahapan. Diantaranya:</p>

        <ul>
            <li>Sikap Atau Cara Berdiri (Open Stance)</li>
            <li>Memasang Ekor Panag (Nocking)</li>
            <li>Mengangkat Lengan (Extend)</li>
            <li>Menarik Tali Busur (Drawing)</li>
            <li>Menjangkarkan Tali Penarik (Anchoring)</li>
            <li>Menahan Sikap Memanah (Tighten/Hold)</li>
            <li>Membidik (Aiming)</li>
            <li>Melepas Tali/Panah (Release) Dan</li>
            <li>Menahan Sikap Memanah (After Hold)</li>


        </ul>
