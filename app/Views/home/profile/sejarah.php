        <h2>Sejarah Siliwangi Archery Club</h2>
        <p>Panahan adalah suatu kegiatan menggunakan busur panah untuk menembakkan anak panah. Bukti-bukti menunjukkan bahwa sejarah panahan telah dimulai sejak 5.000 tahun yang lalu yang awalnya digunakan untuk berburu dan kemudian berkembang sebagai senjata dalam pertempuran dan kemudian sebagai olahraga ketepatan. Seseorang yang gemar atau merupakan ahli dalam memanah disebut juga sebagai pemanah. TIM SAC merupakan nama tim yg di bentuk dari tim sebelumnya yaitu TIM PSACG yg merupakan singkatan dari Patriot Siliwangi Archery TIM Garut yang di dirikan pada tahun 2015. Ini merupakan tim pertama yang hadir di kabupaten garut sebagai gerai pelatihan panahan yang di khususkan untuk pelajar yang terfokuskan untuk pembentukan atlit di kabupaten garut yang di bawahi oleh ketua PERPANI Kabupaten Garut yaitu Bapak Yadi Supriadi. Karena panahan di kabupaten garut semakin popular, maka pengurus dari Tim PSACG ini membentuk kepengurusan baru dengan struktur organisasi yang lengkap pula serta tujuannya yaitu pelatihan ini tidak hanya di fokuskan untuk pembentukan atlit saja melainkan dari umum pun bisa mengikuti pelatihan ini. Maka dari itu pada tahun 2018 berubahlah menjadi TIM SAC singkatannya yaitu Siliwangi Archery TIM. Tim ini dibuka secara resmi oleh Ketua PERPANI Kab.Garut yang sampai sekarang masih berjalan.</p>
        <h2>Sejarah Panahhan Di Dunia</h2>

        <p>Busur telah ditemukan pada masa Paleolitik atau awal periode Mesolitik. Petunjuk tertua akan fungsinya di Eropa datang dari Stellmoor di Lembah Ahrensburg, bagian utara dari Hamburg, Jerman dan tanggal dari akhir Paleolitik, sekitar 10.000-9.000 SM. Panah dibuat dari kayu pinus dan terdiri dari poros utama dan sebuah poros depan sepanjang 15-20 sentimeter atau 6-8 inci dengan sebuah titik batu. Pada zaman dahulu tidak ditemukan busur, sebelumnya ditemukan poros bertitik, tetapi mungkin pada zaman dahulu orang menggunakan pelempar tombak daripada busur. Sejauh ini busur tertua diketahui datang dari rawa Holmegard di Denmark. Akhirnya busur menggantikan pelempar tombak sebagai sarana utama untuk melepaskan batang peluru, dan di setiap benua kecuali Australia, meskipun pelempar tombak bertahan di samping busur di benua Amerika terutama Meksiko (di mana “pelempar tombak” itu atlatl dalam bahasa Nahuatl) dan di antara Suku Inuit. Tapi seperti yang kita tahu busur tertua datang dari Yunani Kuno pada 2800 SM.</p>

        <p>Busur dan panah telah hadir pada budaya Yunani sejak keduanya berasal dari Predinastik (masa sebelum ada kerajaan). Di Levant, artefak yang mungkin menjadi gagang panah pelurus diketahui berasal dari budaya Natufian, ke depan. Orang-orang El-Khiam dan PPN A memanggul Khiampoints mungkin sebagai panah.</p>

        <p>Peradaban klasik, terutama Assirian, Persian, Parthian, Indian, Korean, Cina, Jepang dan Turki menerjunkan pemanah dalam jumlah yang besar pada tentara perangnya. Busur besar Inggris terbukti nilainya untuk pertama kali di Perang Benua ketika Pertempuran Crecy. Pemanah Amerika telah tersebar luas ketika Kontak Eropa.</p>

        <p>Panahan berkembang pesat di Asia. Istilah Sansekerta untuk panahan, dhanurveda, pada saat ini digunakan untuk istilah seni beladiri. Di Asia Timur, Goguryeo, satu dari Tiga Kerajaan Korea, terkenal dengan resimennya sangat berbakat dalam memanah.</p>

        <p>Panahan atau memanah menjadi olahraga yang cukup populer pada zaman sekarang. Sehingga banyak sekali terdiri pelatihan dan juga gerai panahan di kota-kota besar untuk nantinya di pertandingkan. Salah satunya yaitu pelatihan panahan SAC (Siliwangi Archery Club) yang berada di Kabupaten Garut. Dengan kepopulerannya mengenai panahan tentu saja menarik banyak perhatian dan minat dari daerah itu sendiri khususnya di Garut. </p>

        <h3>Perkembangan Panahan di Indonesia</h3>

        <p>Sama halnya dengan Sejarah Panahan di dunia, demikian pula tidak seorang-pun yang dapat memastikan sejak kapan manusia di In-donesia menggunakan Panahan dan busur dalam kehidupannya. Tetapi apabila kita memperhatikan cerita-cerita wayang purwa misalnya, jelas bahwa Sejarah panah dan busur di Indonesia-pun telah cukup pan-jang, dan tokoh-tokoh Pemanah seperti Arjuna, tokoh protagonis dalam wiracarita Mahabharata.</p>

        <p>Kalau PON I kita pakai sebagai batasan waktu era kebangunan Olahraga Nasional, maka Panahan telah ikut ambil bagian dalam era kebangunan Olahraga Nasional itu. Maka PON I adalah Pekan Olahraga Nasional pertama Indonesia yang diadakan di Kota Praja Surakarta pada 9 – 12 September 1948. Tanggal pembukaannya, 9 September, kemudian di-peringati setiap Tahunnya sebagai Hari Olahraga Nasional.</p>

        <p>Pekan Olahraga Nasional I ini diikuti oleh sekitar 600 Atlet yang ber-tanding pada 9 Cabang Olahraga yang memperebutkan sebanyak 108 medali. Pesertanya bukan pada tingkat provinsi melainkan pada tingkat Kota dan Karesidenan, sebanyak 13 partisipan ikut serta. Juaranya ada-lah Karesidenan Surakarta dengan total medali sebanyak 36 medali.</p>

        <p>Dalam Sejarah PON, Panahan merupakan Cabang yang selalu diper-lombakan, walaupun secara resminya Persatuan Panahan Indonesia (PERPANI) baru terbentuk pada tanggal 12 Juli 1953 di Yogyakarta atas prakarsa Sri Paku Alam VIII kemudian diawal pembentukan PER-PANI hanya memiliki Lima (5) anggota yaitu beranggotakan dari wila-yah Surabaya, Jawa Tengah, Yogyakarta, Jakarta dan Madiun. Dan Ke-juaraan Nasional yang pertama sebagai perlombaan yang terorganisir, baru diselenggarakan pada Tahun 1959 di Surabaya.</p>

        <p>Pada saat itu Sri Paku Alam VIII yang merupakan Wakil Kepala Daerah Yogyakarta mendampingi Hamengku Buwono IX. Dan juga sebagai seorang kepala kadipaten Pakualaman Sri Paku Alam VIII juga tertarik dengan olalraga khususııya Panahan, bahkan Ia adalalı tokoh pendiri dari Persatuan Panahan Seluruh Indonesia (PERPANI).</p>

        <p>Sri Paku Alam VIII lalıir di lingkungan Puro Pakualaman, dan naik tahta menggantikan ayahandanya Sri Paku Alan VII, Ia memiliki peran-an sebagai tokoh Olahraga Panahan. Ketertarikan Sri Paku Alam VIII pada Panahan bermula dari hobinya bermain Panahan tradisional Jem-paringan Posisi Mataraman bersama kerabat dan abdi dalem. Dorongan untuk ber-Olahraga juga datang dari dari ibunya Gusti Kanjeng Raden Ayu Retno Puwoso dan kakekııya Paku Buwono X.</p>

        <p>Pada Tahun 1953 Sri Paku Alam VIII mendirikan Persatuan Panahan Indonesia (PERPANI) dan Mardisoro (Perkumpulan Panahan Tradi-sional di Puro Pakualaman). Upaya Sri Paku Alam VIII untuk mengembangkan PERPANI yaitu memperjuangkan Olahraga Panahan sebagai pertandingan resmi yang diperlombakan dalam PON.</p>
        
        <p>Diakuinya Panahan sebagai Olahraga nasional, Perpani semakin mem-perlihatkan prestasinya. Tidak berselang lama Perpani menunjukan prestasinya, beberapa kejuaraan diraih oleh para Atlet Panahan Indone-sia yaitu Tahun 1959, diadakanlah kejuaraan Nasional I di Surabaya, yang merupakan salah satu wilayah dengan perkembangan pesat dalam Olahraga Panahan. Hal ini dapat dilihat dari partisipasi Klub Panahan di Surabaya yang selalu mengadakan pertandingan Panahan. Selain itu beberapa perlombaan maupun seleksi pertandingan juga diselenggara-kan di Surabaya seperti yang tercantum dalam surat edaran bagi seluruh korwil perpani yang ada di Yogyakarta.</p>

        <p>Olahraga Panahan semakin menunjukan perkembangannya, ketika di Tahun 1975 Kelompok Panahan Tradisional Mardisoro diundang oleh Pengda PERPANI DIY untuk mengikuti perlombaan Panahan untuk memperingati Lustrum L.P.P di Yogyakarta yang diselenggarakan pada 21 Januari 1975, perlombaan ini dilaksanakan di Lapangan Kolombo Yogyakarta, (sebelah timur kolam renang Kolombo/ Demangan). Per-lombaan tersebut diikuti oleh beberapa Klub Panahan yang berada di Yogyakarta dengan Cabang yang dilombakan yaitu jarak 50 Meter, 40 Meter, dan 30 Meter.</p>

        <p>Perkembangan Panahan mulai terlihat di wilayah Indonesia terutama di beberapa wilayah bagian seperti Jawa Barat, dan Surabaya. Namun sa-yang baru wilayah-wilayah Jawa saja yang mengalami perkembangan. Panahan sudah menjadi Olahraga yang terorganisir Pada Tahun 1959 di Surabaya. Terorganisirnya Olahraga Panahan di Surabaya, maka se-makin mudah pula untuk mengkoordinir para Atlet berbakat maupun anggota untuk melakukan kerja organisasi dalam hal pemenuhan kebu-tuhan latihan.</p>

        <p>Pada PON ke V yang diselenggarakan di Bandung Tahun 1961, meng-gambarkan perkembangan dari korwil tiap Provinsi. Koran Kedaulatan Rakyat terbitan Oktober 1961 mencatat mengenai hasil akhir pertand-ingan dari masing-masig kategori perlombaan Panahan. Dalam kategori Panahan Puteri posisi berdiri, Panahan Jawa Tengah belum menun-jukan hasilnya.</p>

        <p>Hal ini terbukti dari perolehan juara yang di dominasi oleh Jawa Barat dan dari Jawa Timur. Atlet Panahan Retno Sumilir dan Liem Kim Liang masing-masing menempati Posisi 1, dan 2 dan Posisi ke 3 diraih oleh Rochmah (Jawa Barat). Sedangkan untuk kategori Panahan tunggal Putera posisi berdiri Achmad Tarmidi (Jawa Timur), Hidajat Hadiah (Jawa Barat), dan Ajum Bahrumsyah (Jawa Timur). Regu Puteri Berdiri diperoleh dari sang tuan rumah Jawa Barat dan peringkat ke 2 Jawa Timur. Sedangkan untuk regu Putera berasal dari Jawa Barat, juara 2, Jawa Timur dan ke tiga dari Jawa Tengah.</p>

        <p>Dari hasil juara PON V Tahun 1961 di Bandung dapat dilihat jika Ja-wa Tengah mengalami kemerosotan dalam hal prestasi, sedangkan Jawa Timur yaitu wilayah yang memiliki perhatian yang besar akan Olahraga Panahan menunjukan prestasinya dengan membawa juara paling ban-yak dalam perlombaan Panahan PON ke V.</p>

        <p>Jawa Barat sebagai tuan rumah menunjukan presatasinya dengan menduduki posisi kedua perolehan juara Panahan dalam PON V.
        Penurunan prestasi Atlet Klub Panahan Jawa Tengah tidak ada yang ta-hu pasti apa yang melatar belakangi kemerosotan tersebut, namun Sri Paduka Paku Alam VIII pada Tahun-Tahun tersebut mulai disibukan dengan persiapan Asian Games IV di Jakarta sehingga berpeluang besar jika perhatiannya lebih terfokus pada persiapan Asian Games Tahun 1962.</p>

        

